# boolean
myBool = True

# integer
myInt = int(43)
print(type(myInt), myInt)

# float
myFloat = float(43.1)
print(type(myFloat), myFloat)

# string
myNum = 53
myString = "This is " + str(myNum) + " a string"
print(type(myString), myString)

# tuples
myTuple = (1,2,3,4,5)
print(myTuple)

# lists
myList = [1,2,3,4,5]
myList.append(6)
print(myList)
print(myList[1]) # printing the second element

# dictionaries (otherwise known as hashes in other languages)
myDict = dict(one=1, two=2)
print(myDict['one'])
